    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String bookid=jTextField1.getText();
        ResultSet rs=null;
        if(bookid.equals(""))
        {JOptionPane.showMessageDialog(this,"Book ID Is Compulsory");
        }
        else
        {
         try{
               Connection  con=DriverManager.getConnection("jdbc:mysql://localhost:3306/librarymanagement","root","root");
            PreparedStatement st=con.prepareStatement("select * from add_book where book_id='"+bookid+"'");
            rs=st.executeQuery();
            
          if(rs.next()){
              jTextField2.setText(rs.getString(3));
              jTextField3.setText(rs.getString(2));
              jTextField4.setText(rs.getString(4));
              jTextField5.setText(rs.getString(5));
              jTextField6.setText(rs.getString(6));

              
              st.close();
              con.close();
          }
          else
          {JOptionPane.showMessageDialog(this,"Book Not Found");
          }
         }
         catch(Exception e)
         {JOptionPane.showMessageDialog(this,e);
         }
        
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

